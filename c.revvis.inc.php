<span class="r12r"><strong>RevVis - A Tool for the Visualization </br>of Structures and Properties in Reversible Circuits</strong></span><br>
<p align="justify">
by Robert Wille, Jannis Stoppe, Eleonora Sch&ouml;nborn, Kamalika Datta, and Rolf Drechsler
</p>

<p align="justify">
<!--<img src="../images/RevVisCropped01.jpg" border="0" alt="RevVis screenshot 01" width=100%>-->
RevVis is a graphical interface which visualizes structures and properties of reversible circuits. RevVis collects relevant data of a given reversible cascade and presents it in a simple but intuitive fashion. By this, RevVis unveils information on characteristic structures and properties of reversible circuits that could be utilized for further optimization.</p>


<p align="justify">
The recent interest in reversible computation led to plenty of (automatic) approaches for the design of the corresponding circuits. While this automation is desired in order to provide a proper support for the design of complex functionality, often a manual consideration and human intuition enable improvements or provide new ideas for design solutions. However, this manual interaction requires a good intuition of the structure or the properties of a reversible cascade which, with increasing circuit size, becomes harder to grasp. Visualization techniques that abstract irrelevant details and focus on intuitively displaying important structures or properties provide a solution to this problem and have already successfully been applied in other domains such as design of conventional software, hardware debugging, or Boolean satisfiability.</p>

<p align="justify">
RevVis introduces such a graphical interface to the domain of reversible circuit design. 
RevVis collects relevant data of a given reversible cascade and presents it in a simple but intuitive
fashion. By this, RevVis unveils information on characteristic structures and properties of reversible circuits that could be utilized for further optimization.
</p>





